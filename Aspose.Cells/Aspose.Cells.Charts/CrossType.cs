namespace Aspose.Cells.Charts;

/// <summary>
///       Represents the axis cross type.
///       </summary>
public enum CrossType
{
	/// <summary>
	///       Microsoft Excel sets the axis crossing point.
	///       </summary>
	Automatic,
	/// <summary>
	///       The axis crosses at the maximum value.
	///       </summary>
	Maximum,
	/// <summary>
	///       The axis crosses at the custom value.
	///       </summary>
	Custom
}
