namespace Aspose.Cells;

/// <summary>
///       Enumerates the font underline types.
///       </summary>
public enum FontUnderlineType
{
	/// <summary>
	///       Represents no underline.
	///       </summary>
	None,
	/// <summary>
	///       Represents single underline.
	///       </summary>
	Single,
	/// <summary>
	///       Represents double underline.
	///       </summary>
	Double,
	/// <summary>
	///       Represents single accounting underline.
	///       </summary>
	Accounting,
	/// <summary>
	///       Represents double accounting underline.
	///       </summary>
	DoubleAccounting,
	Dash,
	DashDotDotHeavy,
	DashDotHeavy,
	DashedHeavy,
	DashLong,
	DashLongHeavy,
	DotDash,
	DotDotDash,
	Dotted,
	DottedHeavy,
	Heavy,
	Wave,
	WavyDouble,
	WavyHeavy,
	Words
}
