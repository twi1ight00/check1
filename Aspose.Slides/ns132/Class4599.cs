using System;

namespace ns132;

internal class Class4599
{
	public static double Add(double num1, double num2)
	{
		return num1 + num2;
	}

	public static double smethod_0(double num1, double num2)
	{
		return num1 - num2;
	}

	public static double smethod_1(double num)
	{
		return Math.Abs(num);
	}

	public static double smethod_2(double num)
	{
		return 0.0 - num;
	}

	public static double smethod_3(double num1, double num2)
	{
		return num1 / num2;
	}

	public static double smethod_4(double num1, double num2)
	{
		return num1 * num2;
	}

	public static double smethod_5(double num)
	{
		return Math.Sqrt(num);
	}
}
