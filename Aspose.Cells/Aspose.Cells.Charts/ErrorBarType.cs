namespace Aspose.Cells.Charts;

/// <summary>
///       Represents error bar amount type.
///       </summary>
public enum ErrorBarType
{
	/// <summary>
	///       Custom value type.
	///       </summary>
	Custom,
	/// <summary>
	///       Fixed value type.
	///       </summary>
	FixedValue,
	/// <summary>
	///       Percentage type
	///       </summary>
	Percent,
	/// <summary>
	///       Standard deviation type.
	///       </summary>
	StDev,
	/// <summary>
	///       Standar error type.
	///       </summary>
	StError
}
