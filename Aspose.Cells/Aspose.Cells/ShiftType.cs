namespace Aspose.Cells;

/// <summary>
///       Represent the shift options when deleting a range of cells.
///       </summary>
public enum ShiftType
{
	/// <summary>
	///       Shift cells down.
	///       </summary>
	Down,
	/// <summary>
	///       Shift cells left.
	///       </summary>
	Left,
	/// <summary>
	///       Do not shift cells.
	///       </summary>
	None,
	/// <summary>
	///       Shift cells right.
	///       </summary>
	Right,
	/// <summary>
	///       Shift cells up.
	///       </summary>
	Up
}
