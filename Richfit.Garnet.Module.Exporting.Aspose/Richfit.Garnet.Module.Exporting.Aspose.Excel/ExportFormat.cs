namespace Richfit.Garnet.Module.Exporting.Aspose.Excel;

/// <summary>
/// Excel导出格式枚举
/// </summary>
public enum ExportFormat
{
	/// <summary>
	/// 逗号分割的文本文档（*.csv）
	/// </summary>;
	Csv,
	/// <summary>
	/// Html文档（*.html）
	/// </summary>
	Html,
	/// <summary>
	/// OpenDocument电子表格文档（*.ods）
	/// </summary>
	Ods,
	/// <summary>
	/// PDF格式文档（*.pdf）
	/// </summary>
	Pdf,
	/// <summary>
	/// Excel97至20003格式文档（*.xls）
	/// </summary>
	Xls,
	/// <summary>
	/// Excel2007及以上格式文档（*.xlsx）
	/// </summary>
	Xlsm,
	/// <summary>
	/// Excel2003 SpreadsheetML格式文档（*.xml）
	/// </summary>
	Xml2003,
	/// <summary>
	/// Xps格式文档（*.xps）
	/// </summary>
	Xps
}
