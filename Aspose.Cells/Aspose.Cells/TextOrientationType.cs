namespace Aspose.Cells;

/// <summary>
///       Enumerates text orientation types.
///       </summary>
public enum TextOrientationType
{
	/// <summary>
	///       text is rotated 90 degrees clockwise
	///       </summary>
	ClockWise,
	/// <summary>
	///       text is rotated 90 degrees counterclockwise
	///       </summary>
	CounterClockWise,
	/// <summary>
	///       Represents the default value.
	///       </summary>
	NoRotation,
	/// <summary>
	///       Displays text from top to bottom of the cell.
	///       </summary>
	TopToBottom
}
