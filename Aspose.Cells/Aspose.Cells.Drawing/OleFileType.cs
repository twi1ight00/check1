namespace Aspose.Cells.Drawing;

/// <summary>
///       Ole object file type.
///       </summary>
public enum OleFileType
{
	/// <summary>
	///       Represets the Ms Excel 97-2003 file.
	///       </summary>
	Xls,
	/// <summary>
	///       Represents the Ms word document.
	///       </summary>
	Doc,
	/// <summary>
	///       Represents the Power Point.
	///       </summary>
	Ppt,
	/// <summary>
	///       Represents the Acrobat Document.
	///       </summary>
	Pdf,
	/// <summary>
	///       Represents the MS Equation 3.0 object.
	///       </summary>
	MSEquation,
	/// <summary>
	/// </summary>
	MapiMessage,
	Xlsx,
	Xlsb,
	Docx,
	Pptx,
	/// <summary>
	///       Unknown file type.
	///       </summary>
	Unknown
}
