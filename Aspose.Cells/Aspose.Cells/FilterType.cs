namespace Aspose.Cells;

/// <summary>
///       The filter type.
///       </summary>
public enum FilterType
{
	/// <summary>
	/// </summary>
	ColorFilter,
	/// <summary>
	/// </summary>
	CustomFilters,
	/// <summary>
	/// </summary>
	DynamicFilter,
	/// <summary>
	///       When multiple values are chosen to filter by, or when a group of date values are chosen to filter by, 
	///       this element groups those criteria together. 
	///       </summary>
	MultipleFilters,
	/// <summary>
	/// </summary>
	IconFilter,
	/// <summary>
	/// </summary>
	Top10,
	/// <summary>
	/// </summary>
	None
}
