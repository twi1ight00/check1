namespace Aspose.Cells;

/// <summary>
///       Specifies how to group dateTime values. 
///       </summary>
public enum DateTimeGroupingType
{
	/// <summary>
	///       Group by day. 
	///       </summary>
	Day,
	/// <summary>
	///       Group by hour. 
	///       </summary>
	Hour,
	/// <summary>
	///       Group by Minute.
	///       </summary>
	Minute,
	/// <summary>
	///       Group by Month.
	///       </summary>
	Month,
	/// <summary>
	///       Group by Second.
	///       </summary>
	Second,
	/// <summary>
	///       Group by Year.
	///       </summary>
	Year
}
