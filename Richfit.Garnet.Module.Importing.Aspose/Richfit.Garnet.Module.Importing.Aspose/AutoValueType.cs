namespace Richfit.Garnet.Module.Importing.Aspose;

/// <summary>
/// 自动值类型
/// </summary>
public enum AutoValueType
{
	/// <summary>
	/// 无自动值
	/// </summary>
	None,
	/// <summary>
	/// 默认值
	/// </summary>
	Default,
	/// <summary>
	/// 自动Guid
	/// </summary>
	Guid,
	/// <summary>
	/// 自动获取当前时间
	/// </summary>
	Now,
	/// <summary>
	/// 自动计数
	/// </summary>
	Count
}
