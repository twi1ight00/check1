namespace Aspose.Cells;

/// <summary>
///       Represents whether and how to show objects in the workbook.
///       </summary>
public enum DisplayDrawingObjects
{
	/// <summary>
	///       Show all objects
	///       </summary>
	DisplayShapes,
	/// <summary>
	///       Show placeholders
	///       </summary>
	Placeholders,
	/// <summary>
	///       Hide all shapes.
	///       </summary>
	Hide
}
