namespace Aspose.Cells.Drawing;

/// <summary>
///       Represents the gradient color type for the specified fill.
///       </summary>
public enum GradientColorType
{
	/// <summary>
	///       No gradient color
	///       </summary>
	None,
	/// <summary>
	///       One gradient color
	///       </summary>
	OneColor,
	/// <summary>
	///       Preset gradient colors
	///       </summary>
	PresetColors,
	/// <summary>
	///       Two gradient colors
	///       </summary>
	TwoColors
}
