namespace Aspose.Cells.Charts;

/// <summary>
///       Represents the marker style in a line chart, scatter chart, or radar chart. 
///       </summary>
public enum ChartMarkerType
{
	/// <summary>
	///       Automatic markers.
	///       </summary>
	Automatic,
	/// <summary>
	///       Circular markers.
	///       </summary>
	Circle,
	/// <summary>
	///       Long bar markers
	///       </summary>
	Dash,
	/// <summary>
	///       Diamond-shaped markers.
	///       </summary>
	Diamond,
	/// <summary>
	///       Short bar markers.
	///       </summary>
	Dot,
	/// <summary>
	///       No markers.
	///       </summary>
	None,
	/// <summary>
	///       Square markers with a plus sign.
	///       </summary>
	SquarePlus,
	/// <summary>
	///       Square markers.
	///       </summary>
	Square,
	/// <summary>
	///       Square markers with an asterisk.
	///       </summary>
	SquareStar,
	/// <summary>
	///       Triangular markers.
	///       </summary>
	Triangle,
	/// <summary>
	///       Square markers with an X.
	///       </summary>
	SquareX
}
